/*
Manipulating Elements with JavaScript

1. Compared to CSS, the property names are different in JavaScript. 

    JS uses camelCase instead of hyphenated-lowercase.
    So, In CSS, we use background-color, 
    but in JavaScript we use backgroundColor.

    Thus, document.querySelector("h1").style.backgroundColor = "yellow";

2. In JS the property values are strings, so we need to use quotes, but in CSS we write it directly.
   So, In CSS, we write background-color: yellow;
   but in JS, backgroundColor = "yellow";

   Thus, document.querySelector("h1").style.backgroundColor = "yellow";

*/